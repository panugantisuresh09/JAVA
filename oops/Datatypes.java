public class Datatypes {
    public static void main(String[] args) {
        char a = 'z';
        byte b = 10;
        short c = 32700;
        int d = 2147483647;
        long e = 214748;
        float f = 3.14f;
        double g = 44.121213;
        boolean h = true;
        System.out.println(a + "" + b + "" + c + "" + d + "" + e + "" + f + "" + g + "" + h + "");
    }
}
